package Operator1;

public class Arithmatic {

	public static void main(String[] args) {
		 int a = 5;
		   int b = 6;
		   System.out.println(a+b);
		   System.out.println(a-b);
		   System.out.println(a*b);
		   System.out.println(a/b);
		   System.out.println(a%b);
		   
		   System.out.println(a<b&&b<a);
		   System.out.println(a>b&&b<a);
		   System.out.println(a<b||b<a);
		   System.out.println(a>b||b<a);
		   
			System.out.println(a++ + ++a);
			System.out.println(b-- + --b);

	}

}
